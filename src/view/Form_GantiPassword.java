    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
            String username = t_username.getText();
            String oldPassword = t_passwordLama.getText();
            String newPassword = t_passwordBaru.getText();
            String passwordBaruKonfirmasi = t_passwordBaruKonfirmasi.getText();
            
            if (!newPassword.equals(passwordBaruKonfirmasi)) {
                JOptionPane.showMessageDialog(null, "Konfirmasi password baru tidak sesuai!");
                return;
            }
            
            if (!service.validateOldPassword(username, oldPassword)) {
                JOptionPane.showMessageDialog(null, "Username dan Password lama tidak valid!");
                return;
            }
            
            if (service.changePassword(username, oldPassword, newPassword)) {
                JOptionPane.showMessageDialog(null, "Password berhasil diubah");
                resetForm();
            } else {
                JOptionPane.showMessageDialog(null, "Password gagal diubah!");
            }
            
            
    }//GEN-LAST:event_btn_submitActionPerformed


